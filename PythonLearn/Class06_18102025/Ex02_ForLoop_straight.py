# The range() function in Python works like this:
# range(start, stop, step)

for i in range(1, 10, 1):
    print(i, end=" & ")
print("\n")

for j in range(0, 5):
    print(j, end=" $ ")
print("\n")

for k in range(15):
    print(k, end=" | ")
print("\n")

for l in range(0, 10, 2):
    print(l, end=" & ")
print("\n")