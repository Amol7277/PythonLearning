X = 50
Y = 35

#And gate => If both conditions are true the only it will give the True answer otherwise false.

print(X > 0 and Y > 0)
print(X > 0 and Y < 0)
print(X < 0 and Y < 0)
print(X < 0 and Y > 0)

print("\n\n")

#OR Gate => If one of the condition is true then it will give the True answer otherwise false.

print(X > 0 or Y > 0)
print(X > 0 or Y < 0)
print(X < 0 or Y < 0)
print(X < 0 or Y > 0)

print("\n\n")

#Not Gate => It reverse the Output(Example if condition is true the it will give the false in output)
print(not (X < Y))
print(not (X > Y))

